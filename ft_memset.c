#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	char	*s;
	size_t	i;

	i = 0;
	s = b;
	while (i < n)
	{
		s[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
